package cn.javabase.day08.demo04;

public class Demo03Math {
    public static void main(String[] args) {
        System.out.println(Math.abs(3.14));
        System.out.println(Math.abs(0));
        System.out.println(Math.abs(-2.5));

        System.out.println(Math.ceil(2.4));
        System.out.println(Math.ceil(2.9));
        System.out.println(Math.ceil(2.0));
        System.out.println(Math.ceil(2.5));

        System.out.println(Math.floor(2.1));
        System.out.println(Math.floor(2.9));
        System.out.println(Math.floor(3.0));
        System.out.println(Math.floor(2.5));

        System.out.println(Math.round(2.4));
        System.out.println(Math.round(2.9));
        System.out.println(Math.round(2.0));
        System.out.println(Math.round(2.1));
        System.out.println(Math.round(2.5));
    }
}
